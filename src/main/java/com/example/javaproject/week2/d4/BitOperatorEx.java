package com.example.javaproject.week2.d4;

public class BitOperatorEx {
    public static void main(String[] args) {
        int val1 = 1;
        System.out.println(val1 << 1);
        System.out.println(val1 << 2);

        int val2 = 32;
        System.out.println(val2 >> 1);
        System.out.println(val2 >> 2);

        int val3 = 15;
        System.out.println(val3 >> 1);
        System.out.println(val3 << 2);
    }
}
